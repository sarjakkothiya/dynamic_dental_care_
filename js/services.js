// Get DOM elements
const headerTitleElement = document.querySelector('.main-content-header-title');
const mainContentElement = document.getElementById('main-content');
const navLinksElements = document.querySelectorAll('.main-content-nav-link');

// Function to load content for specific service
function loadContent(serviceId) {
    const content = contentData[serviceId];
    if (content) {
        // Update header title
        headerTitleElement.textContent = content.title;
        
        // Update main content
        mainContentElement.innerHTML = content.content;
        
        // Update active navigation state
        updateActiveNavigation(serviceId);
    }
}

// Function to update active navigation link
function updateActiveNavigation(activeServiceId) {
    navLinksElements.forEach(function(link) {
        link.classList.remove('main-content-nav-link-active');
    });
    
    // Find and activate the correct link
    const activeLink = document.querySelector(`[onclick="loadContent('${activeServiceId}')"]`);
    if (activeLink) {
        activeLink.classList.add('main-content-nav-link-active');
    }
}

